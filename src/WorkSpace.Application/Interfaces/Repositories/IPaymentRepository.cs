using WorkSpace.Domain.Entities;

namespace WorkSpace.Application.Interfaces.Repositories;

public interface IPaymentRepository : IGenericRepositoryAsync<Payment>
{
    Task<Payment?> GetByBookingIdAsync(int bookingId, CancellationToken cancellationToken = default);
    Task<Payment?> GetByTransactionIdAsync(string transactionId, CancellationToken cancellationToken = default);
    Task<List<Payment>> GetPaymentsByStatusAsync(string status, CancellationToken cancellationToken = default);
    Task<List<Payment>> GetPaymentsByDateRangeAsync(DateTimeOffset startDate, DateTimeOffset endDate, CancellationToken cancellationToken = default);
}

